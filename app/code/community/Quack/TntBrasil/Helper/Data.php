class Quack_TntBrasil_Helper_Data extends Mage_Core_Helper_Abstract
{
    public function formatZip($postcode, $length=8)
    {
        $postcode = preg_replace('/[^\d]/', '', $postcode);
        $postcode = str_pad($postcode, $length, '0', STR_PAD_LEFT);
        return $postcode;
    }
}
